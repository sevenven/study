import math

# 常数
print(math.pi)  # 输出圆周率π
print(math.e)  # 输出自然常数e

# 基本数学运算
print(math.sqrt(16))  # 平方根，输出4.0
print(math.pow(2, 3))  # 幂运算，输出8.0
print(math.log(100, 10))  # 对数运算，输出2.0

# 取整或函数
print(math.ceil(4.2))  # 向上取整，输出5
print(math.floor(4.9))  # 向下取整，输出4

# 三角函数
print(math.sin(math.pi / 2))  # 正弦函数，输出1.0
print(math.cos(math.pi))  # 余弦函数，输出-1.0
